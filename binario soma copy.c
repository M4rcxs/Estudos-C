#include <stdio.h>
int main () {
int n1, n2, total, i;
printf("Digite o numero a ser somado: ");
scanf("%d", &n1);
printf("Digite quantas vezes ele vai ser somado: ");
scanf("%d", &n2);
total = n1 * n2;
  while (n1 < total) {
      if (i = total){
          printf("Resultado = %d", i);
      }
      else{
          i = n1+n1;
    }
    break;
  }
return 0;
}